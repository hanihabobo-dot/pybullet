class TestStreamFormat:

    def test_sample_push_config_format(self, streams, registry):
        occluders = [b.id for b in registry.boxels.values()
                     if b.boxel_type == BoxelType.OBJECT]
        if not occluders:
            pytest.skip("No occluders in registry")

        results = list(streams.sample_push_config(occluders[0]))
        assert len(results) >= 1, "Expected at least 1 push config"
        for r in results:
            assert isinstance(r, tuple) and len(r) == 1
            assert isinstance(r[0], RobotConfig)
            assert r[0].name, "name must be non-empty"

    def test_sample_sensing_config_format(self, streams, registry):
        shadows = [b.id for b in registry.boxels.values()
                   if b.boxel_type == BoxelType.SHADOW]
        if not shadows:
            pytest.skip("No shadows in registry")

        results = list(streams.sample_sensing_config(shadows[0]))
        assert len(results) >= 1, "Expected at least 1 sensing config"
        for r in results:
            assert isinstance(r, tuple) and len(r) == 1
            assert isinstance(r[0], RobotConfig)
            assert r[0].name

    def test_sample_grasp_format(self, streams):
        results = list(streams.sample_grasp("any_object"))
        assert len(results) == 4, f"Expected 4 grasps, got {len(results)}"
        for r in results:
            assert isinstance(r, tuple) and len(r) == 1
            assert isinstance(r[0], Grasp)
            assert r[0].name
            assert abs(np.linalg.norm(r[0].orientation) - 1.0) < 1e-6

    def test_plan_motion_format(self, streams):
        q1 = RobotConfig(np.zeros(7), name="q_a")
        q2 = RobotConfig(np.ones(7) * 0.1, name="q_b")
        results = list(streams.plan_motion(q1, q2))
        assert len(results) == 1
        traj = results[0][0]
        assert isinstance(traj, Trajectory)
        assert len(traj.waypoints) == 10
        assert traj.name

    def test_compute_kin_format(self, streams, registry):
        shadows = [b.id for b in registry.boxels.values()
                   if b.boxel_type == BoxelType.SHADOW]
        if not shadows:
            pytest.skip("No shadows in registry")
        grasp = Grasp(np.array([0, 0, 0.05]),
                      np.array([0, 0, 0, 1]), name="test_g")
        results = list(streams.compute_kin_solution("obj_test", shadows[0], grasp))
        # May be 0 if IK fails for this boxel — that's fine
        for r in results:
            assert isinstance(r, tuple) and len(r) == 1
            assert isinstance(r[0], RobotConfig)
            assert r[0].name

    def test_nonexistent_boxel_yields_nothing(self, streams):
        assert list(streams.sample_push_config("nonexistent_boxel")) == []
        assert list(streams.sample_sensing_config("nonexistent_boxel")) == []
        grasp = Grasp(np.zeros(3), np.array([0, 0, 0, 1]), name="g")
        assert list(streams.compute_kin_solution("o", "nonexistent_boxel", grasp)) == []


