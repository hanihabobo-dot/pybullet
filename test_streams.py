# 6. Reachability report (diagnostic, always passes)
# ===========================================================================

def test_reachability_report(streams, registry, capsys):
    """
    Diagnostic: for every occluder and shadow, try the streams and report
    how many IK solutions pass FK verification. This always 'passes' but
    prints a table useful for debugging planning failures.
    """
    print("\n" + "=" * 70)
    print("REACHABILITY REPORT  (FK-verified IK solutions per boxel)")
    print("=" * 70)

    occluders = sorted(b.id for b in registry.boxels.values()
                       if b.boxel_type == BoxelType.OBJECT)
    shadows = sorted(b.id for b in registry.boxels.values()
                     if b.boxel_type == BoxelType.SHADOW)

    print(f"\n{'Boxel ID':<16} {'Type':<10} {'Center':^30} {'IK OK':>6} {'IK Tried':>9}")
    print("-" * 70)

    total_ok, total_tried = 0, 0

    for occ_id in occluders:
        boxel = registry.get_boxel(occ_id)
        center = boxel.center
        results = list(streams.sample_push_config(occ_id))
        ok = len(results)
        tried = 4  # 4 angles attempted
        total_ok += ok
        total_tried += tried
        print(f"{occ_id:<16} {'occluder':<10} [{center[0]:.2f}, {center[1]:.2f}, {center[2]:.2f}]"
              f"{'':>6}{ok:>6} / {tried}")

    for shd_id in shadows:
        boxel = registry.get_boxel(shd_id)
        center = boxel.center
        results = list(streams.sample_sensing_config(shd_id))
        ok = len(results)
        tried = 8 * 3  # 8 angles x 3 heights
        total_ok += ok
        total_tried += tried
        print(f"{shd_id:<16} {'shadow':<10} [{center[0]:.2f}, {center[1]:.2f}, {center[2]:.2f}]"
              f"{'':>6}{ok:>6} / {tried}")

    print("-" * 70)
    print(f"{'TOTAL':<16} {'':10} {'':30} {total_ok:>6} / {total_tried}")
    if total_tried > 0:
        print(f"Overall FK-verified success rate: {100*total_ok/total_tried:.1f}%")
    print("=" * 70)
