class TestMathUtils:

    @pytest.fixture(autouse=True)
    def _make_streams(self):
        """Minimal BoxelStreams with no robot (math helpers don't need one)."""
        dummy_reg = BoxelRegistry()
        self.s = BoxelStreams(dummy_reg, robot_id=None, physics_client=None)

    def test_direction_to_quat_unit(self):
        for d in [np.array([0, 0, -1.0]),
                  np.array([1, 0, 0.0]),
                  np.array([0.3, -0.7, 0.5])]:
            q = self.s._direction_to_quat(d / np.linalg.norm(d))
            assert abs(np.linalg.norm(q) - 1.0) < 1e-6, f"non-unit quat for dir={d}"

    def test_rotation_matrix_to_quat_identity(self):
        R = np.eye(3)
        q = self.s._rotation_matrix_to_quat(R)
        assert abs(np.linalg.norm(q) - 1.0) < 1e-6
        # Identity rotation -> quat [0,0,0,1]
        assert abs(q[3]) > 0.99, f"expected w≈1 for identity, got {q}"

    def test_rotation_matrix_to_quat_90z(self):
        """90-degree rotation about Z."""
        R = np.array([[0, -1, 0],
                      [1,  0, 0],
                      [0,  0, 1]], dtype=float)
        q = self.s._rotation_matrix_to_quat(R)
        assert abs(np.linalg.norm(q) - 1.0) < 1e-6
        # Expected: [0, 0, sin(45°), cos(45°)] = [0, 0, 0.7071, 0.7071]
        assert abs(abs(q[2]) - math.sin(math.pi / 4)) < 1e-4
        assert abs(abs(q[3]) - math.cos(math.pi / 4)) < 1e-4

    def test_euler_to_quat_matches_pybullet(self):
        """Compare our Euler->quat with PyBullet's reference implementation."""
        for roll, pitch, yaw in [(0, 0, 0), (0, math.pi, 0),
                                  (0.1, -0.3, 1.2), (math.pi/2, 0, math.pi/4)]:
            ours = self.s._euler_to_quat(roll, pitch, yaw)
            ref = np.array(p.getQuaternionFromEuler([roll, pitch, yaw]))
            assert np.allclose(ours, ref, atol=1e-6), \
                f"euler ({roll},{pitch},{yaw}): ours={ours} ref={ref}"


