class TestIK:

    def test_reachable_position(self, streams, pybullet_env):
        """A position in front of and above the robot base should be reachable."""
        ee_pos = np.array([0.5, 0.0, 0.5])
        ee_orn = np.array(p.getQuaternionFromEuler([0, math.pi, 0]))
        config = streams._pybullet_ik(ee_pos, ee_orn)

        assert config is not None, "IK returned None for a reachable target"
        assert config.joint_positions.shape == (7,)
        # Joint limits
        assert np.all(config.joint_positions >= streams.joint_limits_low - 0.01)
        assert np.all(config.joint_positions <= streams.joint_limits_high + 0.01)

    def test_fk_matches_target(self, streams, pybullet_env):
        """FK of the returned joints should be within 5 cm of the request."""
        ee_pos = np.array([0.4, 0.1, 0.4])
        ee_orn = np.array(p.getQuaternionFromEuler([0, math.pi, 0]))
        config = streams._pybullet_ik(ee_pos, ee_orn)
        if config is None:
            pytest.skip("IK didn't converge for this target — acceptable")

        client = pybullet_env["client"]
        robot = pybullet_env["robot_id"]
        state_id = p.saveState(physicsClientId=client)
        for i in range(7):
            p.resetJointState(robot, i, config.joint_positions[i],
                              physicsClientId=client)
        actual = np.array(p.getLinkState(robot, 11,
                                          physicsClientId=client)[0])
        p.restoreState(state_id, physicsClientId=client)
        p.removeState(state_id, physicsClientId=client)

        error = np.linalg.norm(actual - ee_pos)
        assert error < 0.08, f"FK error {error:.4f} m exceeds 8 cm threshold"

    def test_unreachable_position(self, streams):
        """A target far outside the workspace should return None."""
        ee_pos = np.array([5.0, 5.0, 5.0])
        ee_orn = np.array([0, 0, 0, 1])
        assert streams._pybullet_ik(ee_pos, ee_orn) is None


