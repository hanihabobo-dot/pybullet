class TestStreamLogging:

    def test_logged_counts_results(self):
        """_logged wrapper tracks call count and result count."""
        from pddlstream_planner import PDDLStreamPlanner
        from boxel_data import BoxelRegistry

        planner = PDDLStreamPlanner(BoxelRegistry())
        planner._init_stream_stats()

        def gen(x):
            yield ("a",)
            yield ("b",)

        logged = planner._logged('sample-grasp', gen)
        results = list(logged("obj1"))
        assert len(results) == 2
        assert planner._stream_stats['sample-grasp']['calls'] == 1
        assert planner._stream_stats['sample-grasp']['results'] == 2

    def test_logged_does_not_cap(self):
        """_logged passes through all results without capping."""
        from pddlstream_planner import PDDLStreamPlanner
        from boxel_data import BoxelRegistry

        planner = PDDLStreamPlanner(BoxelRegistry())
        planner._init_stream_stats()

        def gen(x):
            for i in range(20):
                yield (i,)

        logged = planner._logged('sample-grasp', gen)
        results = list(logged("test"))
        assert len(results) == 20


