# 1. Dataclass hash / eq / repr
# ===========================================================================

class TestDataclasses:

    def test_robot_config_hashable(self):
        c1 = RobotConfig(joint_positions=np.zeros(7), name="q1")
        c2 = RobotConfig(joint_positions=np.ones(7), name="q1")
        assert hash(c1) == hash(c2)
        assert c1 == c2
        assert len({c1, c2}) == 1

    def test_robot_config_different_names(self):
        c1 = RobotConfig(joint_positions=np.zeros(7), name="a")
        c2 = RobotConfig(joint_positions=np.zeros(7), name="b")
        assert c1 != c2
        assert len({c1, c2}) == 2

    def test_robot_config_repr(self):
        c = RobotConfig(joint_positions=np.zeros(7), name="q_home")
        assert repr(c) == "q_home"

    def test_grasp_hashable(self):
        g1 = Grasp(np.zeros(3), np.array([0, 0, 0, 1]), name="g1")
        g2 = Grasp(np.ones(3), np.array([1, 0, 0, 0]), name="g1")
        assert g1 == g2
        assert len({g1, g2}) == 1

    def test_grasp_repr(self):
        g = Grasp(np.zeros(3), np.array([0, 0, 0, 1]), name="grasp_A")
        assert repr(g) == "grasp_A"

    def test_trajectory_hashable(self):
        t1 = Trajectory(waypoints=[], name="t1")
        t2 = Trajectory(waypoints=[RobotConfig(np.zeros(7))], name="t1")
        assert t1 == t2
        assert len({t1, t2}) == 1

    def test_trajectory_repr(self):
        t = Trajectory(waypoints=[None, None, None], name="traj_3")
        assert repr(t) == "traj_3"

    def test_eq_returns_not_implemented_for_foreign_type(self):
        c = RobotConfig(joint_positions=np.zeros(7), name="x")
        assert c.__eq__("not a config") is NotImplemented


